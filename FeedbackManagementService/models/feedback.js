const mongoose = require("mongoose");

const feedbackSchema = new mongoose.Schema({
 
      user: {
        type: mongoose.Schema.ObjectId,
        ref: "User",
        required: true,
      },
      name: {
        type: String,
        required: true,
      },
      rating: {
        type: Number,
        required: true,
      },
      comment: {
        type: String,
        required: true,
      },
      createdAt: {
        type: Date,
        default: Date.now,
      }
    
});

module.exports = mongoose.model("Feedback", feedbackSchema);
